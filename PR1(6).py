# Ініціалізуємо два вектори розміром 4 та заповнюємо їх довільними значеннями
vector1 = [2.0, 4.0, 6.0, 8.0]
vector2 = [1.0, 3.0, 5.0, 7.0]

# Ініціалізуємо змінну для зберігання скалярного добутку
dot_product = 0.0

# Обчислюємо скалярний добуток векторів
for i in range(len(vector1)):
    dot_product += vector1[i] * vector2[i]

# Виводимо результат скалярного добутку
print("Скалярний добуток векторів:", dot_product)
