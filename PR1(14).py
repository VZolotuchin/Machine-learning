import numpy as np

# Ініціалізація розміру матриці
rows = 6
cols = 6

# Заповнення матриці випадковими цілими числами від -50 до 50
matrix = np.random.randint(-50, 51, size=(rows, cols))

# Виведення матриці
print("Матриця:")
print(matrix)

# Обчислення середнього арифметичного значення елементів матриці
average_value = np.mean(matrix)

# Виведення результату
print("Середнє арифметичне значення елементів матриці:", average_value)
